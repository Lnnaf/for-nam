    private void btnDeleteActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnDeleteActionPerformed
        // TODO add your handling code here:
         int selectedIndex = tblNhanvien.getSelectedRow();
        if(selectedIndex >= 0) {
            nv std = nvList.get(selectedIndex);
            
            int option = JOptionPane.showConfirmDialog(this, "Do you want to delete this item?");
            System.out.println("option : " + option);
            
            if(option == 0) {
                nvModify.delete(std.getId());
                
                shownv();
            }
        }
    }//GEN-LAST:event_btnDeleteActionPerformed

    private void btnFindActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnFindActionPerformed
        // TODO add your handling code here:
        String input = JOptionPane.showInputDialog(this, "Enter full name to search");
        if(input!= null && input.length() >0){
            nvList = nvModify.findByfullname(input);
           tableModel.setRowCount(0);

        nvList.forEach((nv) -> {
            tableModel.addRow(new Object[]{tableModel.getRowCount() + 1, nv.getFullname(),
                nv.getGioitinh(), nv.getCv(), nv.getTuoi(), nv.getEmail(), nv.getSdt(), nv.getDc()});
        });
        }else {
            shownv();
        }    
    }//GEN-LAST:event_btnFindActionPerformed

