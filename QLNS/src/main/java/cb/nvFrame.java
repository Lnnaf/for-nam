    private void btnSaveActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnSaveActionPerformed
        // TODO add your handling code here:
        String fullname = txtFullname.getText();
        String gioitinh = cbGender.getSelectedItem().toString();
        String cv = cbJob.getSelectedItem().toString();
        int tuoi = Integer.parseInt(txtAge.getText());
        String email = txtEmail.getText();
        String sdt = txtNumberPhone.getText();
        String dc = txtAdress.getText();
        
        nv std = new nv(fullname, gioitinh, cv, email, sdt, dc, tuoi);
        
        
        nvModify.insert(std);
        
        shownv();
    }//GEN-LAST:event_btnSaveActionPerformed

